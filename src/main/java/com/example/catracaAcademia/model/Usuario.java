package com.example.catracaAcademia.model;

public class Usuario {

    String nome;
    Integer idade;
    String email;

    public String getNome() {
        return nome;

    }

    public Integer getIdade() {
        return idade;

    }

    public void setNome(String nome) {
        this.nome = nome;

    }

    public void setIdade(Integer idade) {
        this.idade = idade;

    }

    public String getEmail() {
        return email;

    }
}
